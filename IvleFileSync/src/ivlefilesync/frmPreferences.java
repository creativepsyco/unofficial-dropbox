    private void checkSyncDirExists() {
        syncDirString = IVLEOfflineStorage.GetPropertyValue(Constants.SyncDirectory);
        if(syncDirString != null || syncDirString != "") {
            jTextField3.setText(syncDirString);
        } else {
            jTextField3.setText("Please Select a Sync Directory.");
        }
    }

    private void checkAPIKeyAndUserID() {
        stringUserId = IVLEOfflineStorage.GetPropertyValue(Constants.UserID);
        APIKeyString = IVLEOfflineStorage.GetPropertyValue(Constants.APIKey);
        if (stringUserId != null || stringUserId != "") {
            jTextField1.setText(stringUserId);
        }

        if (APIKeyString != null || APIKeyString != "") {
            jTextField2.setText(APIKeyString);
        } 
    }
